CREATE VIEW [dbo].[_Fluig_ITENS_ALMOX]

AS

-- DEFININDO UM ITEM DE ALMOXARIFADO
-- CAMPOS DO FORMULÁRIO, FILIAL, CENTRO DE CUSTO, OBSERVAÇÃO E ITENS
SELECT CODFILIAL, TPRODUTO.IDPRD, TPRODUTO.CODIGOPRD, TPRODUTO.NOMEFANTASIA, TPRODUTO.DESCRICAO
	FROM TPRODUTO 
		JOIN TPRDFIL ON (TPRODUTO.IDPRD=TPRDFIL.IDPRD)
WHERE INATIVO	<>	1
AND ULTIMONIVEL	=	1
AND	TPRDFIL.ESTOCAVEL = 1

GO


