CREATE VIEW [dbo].[_Fluig_CUSTO] AS

SELECT 

	GCCUSTO.CODCCUSTO
,	GCCUSTO.NOME
,	GCCUSTO.CODCCUSTO+' - '+GCCUSTO.NOME AS CUSTO_NOME
,	CODREDUZIDO

FROM GCCUSTO(NOLOCK) 
	
WHERE ATIVO='T' AND PERMITELANC='T'

GO

